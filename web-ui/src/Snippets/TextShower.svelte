    import type {File} from "../Struct.svelte";

    export let url: string;
    export let file: File;

    $: contents = "";

    onMount(async () => {
        contents = await (await fetch(url, {
            headers: {
                "x-pupcloud-session": sessionStorage.getItem("x-pupcloud-session"),
            },
        })).text();
    });
